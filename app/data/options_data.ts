export const calculatePoints = (properties: GeoJSON.GeoJsonProperties, option: typeof OPTIONS_DATA[number]) => {
  const biodiversityLevel = properties?.P_groen_pu || 0
  let totalPoints = 0
  totalPoints += (option.impact_value + biodiversityLevel) * 5
  totalPoints += (10 - option.minimum_effort) * 10
  totalPoints += option.minimum_cost
  return Math.round(totalPoints / 10) * 10
}

export const getPointColor = (points: number) => {
  if (points >= 200) return "bg-green-500"
  if (points >= 120) return "bg-yellow-500"
  return "bg-red-500"
}

