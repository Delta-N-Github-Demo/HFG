export const calculatePoints = (properties: GeoJSON.GeoJsonProperties, impact: number, effort: number) => {
  const biodiversityLevel = properties?.P_groen_pu || 0
  return Math.round(((impact + biodiversityLevel) * 5 + (10 - effort) * 10) / 10) * 10
}

export const getPointColor = (points: number) => {
  if (points >= 100) return "bg-green-500"
  if (points >= 80) return "bg-yellow-500"
  return "bg-red-500"
}

